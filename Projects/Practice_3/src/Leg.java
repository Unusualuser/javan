public class Leg {
    double length;

    public Leg(double length) {
        this.length = length;
    }

    public double getLength() {
        return length;
    }

    public void setLength(double length) {
        this.length = length;
    }

    @java.lang.Override
    public java.lang.String toString() {
        return "Leg{" +
                "length=" + length +
                '}';
    }
}
