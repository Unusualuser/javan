public class Head {
    boolean haired;

    public Head(boolean haired) {
        this.haired = haired;
    }

    public boolean getHaired() {
        return haired;
    }

    public void setHaired(boolean haired) {
        this.haired = haired;
    }

    @java.lang.Override
    public java.lang.String toString() {
        return "Head{" +
                "haired=" + haired +
                '}';
    }
}
